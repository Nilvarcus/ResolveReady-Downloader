def run_handbrake_conversion(source_filepath):
    """
    Converts a single video file using HandBrake CLI.

    This function builds the command, runs the HandBrake process, filters its
    output to show only a clean progress bar, and handles success or failure.

    Args:
        source_filepath (str): The full path to the video file to be converted.
    """
    # 1. --- Validation ---
    # Ensure that the required HandBrake files exist before attempting to run.
    if not os.path.exists(HANDBRAKE_CLI_PATH) or not os.path.exists(HANDBRAKE_PRESET_FILE):
        print("\nERROR: HandBrakeCLI.exe or resolve_preset.json could not be found in the application bundle.")
        return

    print(f"\n--- Starting HandBrake conversion... ---")

    # 2. --- Path Construction ---
    # Define where the final, converted file will be saved.
    output_dir = os.path.join(APP_DIR, HANDBRAKE_OUTPUT_FOLDER)
    os.makedirs(output_dir, exist_ok=True) # Create the output folder if it doesn't exist.

    # Create a new filename for the converted video.
    base_filename = os.path.splitext(os.path.basename(source_filepath))[0]
    output_filepath = os.path.join(output_dir, f"{base_filename}_Handbraked.mp4")

    # 3. --- Command Assembly ---
    # Create the list of arguments to pass to the HandBrake command-line interface.
    command = [
        HANDBRAKE_CLI_PATH,
        "-i", source_filepath,
        "-o", output_filepath,
        "--preset-import-file", HANDBRAKE_PRESET_FILE,
        "-Z", HANDBRAKE_PRESET_NAME
    ]

    # 4. --- Process Execution and Output Filtering ---
    # Use subprocess.Popen to run HandBrake and capture its output in real-time.
    # This gives us fine-grained control over what is displayed to the user.
    process = subprocess.Popen(
        command,
        stdout=subprocess.PIPE,        # Capture standard output.
        stderr=subprocess.STDOUT,       # Redirect errors to the same stream as standard output.
        universal_newlines=True,      # Decode output as text.
        encoding='utf-8',             # Specify the text encoding.
        # This flag prevents a new console window from flashing on Windows.
        creationflags=subprocess.CREATE_NO_WINDOW if sys.platform == 'win32' else 0
    )

    # Read the output line-by-line as it's generated by HandBrake.
    for line in iter(process.stdout.readline, ''):
        # Filter the output: only show the line containing the encoding progress.
        if "Encoding: task" in line:
            # Print the line, but use '\r' (carriage return) as the end character.
            # This moves the cursor to the beginning of the current line, creating
            # an animated progress bar effect instead of spamming new lines.
            print(line.strip(), end='\r')

    process.stdout.close()
    return_code = process.wait() # Wait for the HandBrake process to fully complete.
    print() # Print a final newline to move past the progress bar.

    # 5. --- Result Handling and Cleanup ---
    if return_code == 0: # A return code of 0 means the process succeeded.
        print(f"\nSuccessfully encoded! File saved in the '{HANDBRAKE_OUTPUT_FOLDER}' folder.")
        try:
            os.remove(source_filepath) # Delete the original downloaded file.
        except OSError as e:
            print(f"ERROR: Failed to delete original file '{source_filepath}'. {e}")
    else: # Any other return code indicates an error.
        print("\n--- HANDBRAKE ERROR ---")
        print(f"Encoding failed. HandBrake exited with error code {return_code}.")


def download_youtube_video(url):
    """
    Downloads a YouTube video in the best available quality using yt-dlp.

    After a successful download, it triggers the HandBrake conversion process
    via a postprocessor hook.

    Args:
        url (str): The YouTube URL to download.
    """
    # This is a "hook" function that yt-dlp will call automatically
    # after it finishes a download and merge operation.
    def handbrake_postprocessor_hook(d):
        if d['status'] == 'finished':
            # Get the full path of the successfully downloaded file.
            filepath = d.get('info_dict', {}).get('filepath')
            if filepath and os.path.exists(filepath):
                # If the file exists, pass it to our conversion function.
                run_handbrake_conversion(filepath)

    # Configuration options for the yt-dlp downloader.
    yt_dlp_options = {
        # Select the best quality video and audio streams and merge them.
        'format': 'bestvideo+bestaudio/best',
        # Define the output filename template and save location (next to the .exe).
        'outtmpl': os.path.join(APP_DIR, '%(title)s.%(ext)s'),
        # Specify the final container format after merging.
        'merge_output_format': 'mp4',
        # Register our custom function to run after processing is complete.
        'postprocessor_hooks': [handbrake_postprocessor_hook],
        # Suppress all informational output from yt-dlp except for the progress bar.
        'quiet': True,
        'no_warnings': True,
    }

    # Execute the download. 'with' statement ensures resources are handled correctly.
    with yt_dlp.YoutubeDL(yt_dlp_options) as ydl:
        print(f"--- Starting YouTube download for: {url} ---")
        ydl.download([url])


