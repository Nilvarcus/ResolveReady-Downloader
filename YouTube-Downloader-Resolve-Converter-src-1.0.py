# --- Global Constants ---
# These values can be easily changed to alter the application's behavior.
HANDBRAKE_PRESET_NAME = "Resolve Ready"
HANDBRAKE_OUTPUT_FOLDER = "Handbraked"

# --- Dynamic Path Resolution ---
# Determines if the script is running as a standalone .exe or a normal .py file.
# This is crucial for finding bundled data files like HandBrakeCLI.exe.
IS_RUNNING_AS_EXE = getattr(sys, 'frozen', False)

if IS_RUNNING_AS_EXE:
    # If running as a bundled .exe created by PyInstaller:
    # APP_DIR is the directory where the user ran the .exe file from.
    # This is where we will look for videos and save the output.
    APP_DIR = os.path.dirname(sys.executable)
    # BUNDLE_DIR is the path to the temporary folder where PyInstaller
    # has extracted the bundled files (like HandBrakeCLI).
    BUNDLE_DIR = sys._MEIPASS
else:
    # If running as a standard .py script:
    # The application and bundle directories are the same.
    APP_DIR = os.path.dirname(os.path.abspath(__file__))
    BUNDLE_DIR = APP_DIR

# --- Asset Paths ---
# Build the full paths to the required executable and preset file.
# These assets are expected to be bundled with the application.
HANDBRAKE_CLI_PATH = os.path.join(BUNDLE_DIR, "HandBrakeCLI.exe")
HANDBRAKE_PRESET_FILE = os.path.join(BUNDLE_DIR, "resolve_preset.json")


